// arithmetic operators
// + - * / ve % , bildiğimiz işlem uygulayıcıları

fn main() {
    let x = 5;
    let y = 2;
    
    println!("x = {} ve y = {}", x , y);
    println!("Topla => x + y = {}", x + y);
    println!("Çıkar => x - y = {}", x - y);
    println!("Çarp => x * y = {}", x * y);
    println!("Tam Böl => x / y = {}", x / y);
    println!("Bölümden Kalanı Bul (Mod Al)=> {}", x % y);
}

/*
Sonuçlar aşağıda: 

x = 5 ve y = 2
Topla => x + y = 7
Çıkar => x - y = 3
Çarp => x * y = 10
Tam Böl => x / y = 2
Bölümden Kalanı Bul (Mod Al)=> 1

*/
