//karşılaştırma işlemcileri, comparison operators
// <, >, <=, >=, ==, !=
// == eşitttir ve != eşit değil
//true ya da false dönüş olur
  
fn main() {
    let x = 5;
    let y = 10;
    println!("x > y => {}", x > y); 
    println!("x < y => {}", x < y); 
    println!("x >= y => {}", x >= y);
    println!("x <= y => {}", x <= y);
    println!("x == y => {}", x == y);
    println!("x != y => {}", x != y);
}
                                    
/*
işlem sonuçları
                                   
x > y => false
x < y => true
x >= y => false
x <= y => true
x == y => false
x != y => true
  
*/

  
